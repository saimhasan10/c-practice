#include<stdio.h>

int main()
{

    // use of sizeof to know the size
    int i;
    double d;
    float f;
    char c;

    printf("size of the integer: %d bytes\n", sizeof(i));
    printf("size of the double: %d bytes\n", sizeof(d));
    printf("size of the float: %d bytes\n", sizeof(f));
    printf("size of the character: %d bytes\n", sizeof(c));



    //
}
